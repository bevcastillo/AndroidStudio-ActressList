package com.example.beverly.actresslist;

public class Names {

    private int img;
    private String name;

    public Names(int img, String name) {
        super();
        this.img = img;
        this.name = name;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
